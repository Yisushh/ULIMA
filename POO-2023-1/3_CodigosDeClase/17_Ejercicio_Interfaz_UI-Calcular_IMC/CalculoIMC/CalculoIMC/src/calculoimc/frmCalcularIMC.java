    private boolean validarDatos() {
        if (this.txtPeso.getText().equals("") || this.txtAltura.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

    private void btnCalcularIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularIMCActionPerformed
        // TODO add your handling code here:
        if (validarDatos()) {
            double peso = Double.parseDouble(this.txtPeso.getText());
            double altura = Double.parseDouble(this.txtAltura.getText());

            Double IMC = peso / (altura * altura);
            this.lblIMC.setText(IMC.toString());

            String diagnostico = "";
            if (IMC < 16) {
                diagnostico = "Criterio de ingreso en hospital";
            }
            if (IMC >= 16 && IMC < 17) {
                diagnostico = "Infrapeso";
            }
            if (IMC >= 17 && IMC < 18) {
                diagnostico = "bajo peso";
            }
            if (IMC >= 18 && IMC < 25) {
                diagnostico = "Peso normal (saludable)";
            }
            if (IMC >= 25 && IMC < 30) {
                diagnostico = "Sobrepeso (obesidad grado I)";
            }
            if (IMC >= 30 && IMC < 35) {
                diagnostico = "Sobrepeso cr贸nico (obesidad grado II)";
            }
            if (IMC >= 35 && IMC < 40) {
                diagnostico = "Obesidad prem贸rbida (obesidad grado III)";
            }
            if (IMC >= 40) {
                diagnostico = "Obesidad m贸rbida (obesidad grado IV)";
            }
            
            this.lblDiagnostico.setText(diagnostico);

        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar el peso y la altura");
        }


    }//GEN-LAST:event_btnCalcularIMCActionPerformed

